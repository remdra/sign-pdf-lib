import { PDFDocument } from 'pdf-lib';

export async function generatePdfLibPdfAsync(useObjectStreams: boolean = false): Promise<Buffer> {
    const pdfDoc = await PDFDocument.create();
    pdfDoc.setProducer('SignPdfLib');
    pdfDoc.setCreator('SignPdfLib');
    pdfDoc.setCreationDate(new Date(2023, 1, 23, 9, 0, 0));
    pdfDoc.setModificationDate(new Date(2023, 1, 23, 9, 0, 0));

    const page = pdfDoc.addPage();
    const fontSize = 10;
    page.drawText('PDF signing test document generated by PDFLib.', {
        x: 50,
        y: page.getSize().height - 4 * fontSize,
        size: fontSize
    });

    return Buffer.from(await pdfDoc.save({ useObjectStreams }));
}

function getText(pageIndex: number, pageCount: number) {
    return pageCount == 1
        ? 'PDF signing test document generated by PDFLib.'
        : `[Page ${pageIndex + 1}] PDF signing test document generated by PDFLib.`;
}

export async function generatePdfAsync({ pageCount, useObjectStreams }: { pageCount?: number; useObjectStreams?: boolean }): Promise<Buffer> {
    const pdfDoc = await PDFDocument.create();
    pdfDoc.setProducer('SignPdfLib');
    pdfDoc.setCreator('SignPdfLib');
    pdfDoc.setCreationDate(new Date(2023, 1, 23, 9, 0, 0));
    pdfDoc.setModificationDate(new Date(2023, 1, 23, 9, 0, 0));

    pageCount = pageCount || 1;
    for(let i = 0; i < pageCount; i++) {
        const page = pdfDoc.addPage();
        const fontSize = 10;
        const text = getText(i, pageCount);
        page.drawText(text, {
            x: 50,
            y: page.getSize().height - 4 * fontSize,
            size: fontSize
        });
    }

    useObjectStreams = useObjectStreams == undefined ? false : useObjectStreams;
    return Buffer.from(await pdfDoc.save({ useObjectStreams }));
}
